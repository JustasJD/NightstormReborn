    /// Weapon for attacking.
    /// </summary>
    Weapon = 1,

    /// <summary>
    /// Armor for defense.
    /// </summary>
    Armor = 2,

    /// <summary>
    /// Consumable item (potions, food, etc.).
    /// </summary>
    Consumable = 3,

    /// <summary>
    /// Quest-related item.
    /// </summary>
    Quest = 4,

    /// <summary>
    /// Crafting material.
    /// </summary>
    Material = 5,

    /// <summary>
    /// Accessory (rings, amulets, etc.).
    /// </summary>
    Accessory = 6,

    /// <summary>
    /// Miscellaneous item.
    /// </summary>
    Miscellaneous = 7
}
