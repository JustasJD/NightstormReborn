namespace Nightstorm.Core.Enums;

/// <summary>
/// Represents the biome/climate type of a zone.
/// </summary>
public enum BiomeType
{
    /// <summary>
    /// Frozen wastelands and ice castles (Rows A-B).
    /// </summary>
    NorthernFrost = 1,
    
    /// <summary>
    /// Cold forests and mountains (Rows C-D).
    /// </summary>
    IronTaiga = 2,
    
    /// <summary>
    /// Desert with sand dunes and oases (Row E).
    /// </summary>
    BurningEquator = 3,
    
    /// <summary>
    /// Volcanic ash and wastelands (Rows F-G).
    /// </summary>
    AshenSteppes = 4,
    
    /// <summary>
    /// Antarctic eternal winter (Rows H-I).
    /// </summary>
    SouthernFreeze = 5
}
