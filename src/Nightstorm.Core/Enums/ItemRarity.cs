namespace Nightstorm.Core.Enums;

/// <summary>
/// Represents the rarity levels of items in the game.
/// </summary>
public enum ItemRarity
{
    /// <summary>
    /// Common items with basic stats.
    /// </summary>
    Common = 1,

    /// <summary>
    /// Uncommon items with slightly enhanced stats.
    /// </summary>
    Uncommon = 2,

    /// <summary>
    /// Rare items with significantly enhanced stats.
    /// </summary>
    Rare = 3,

    /// <summary>
    /// Epic items with powerful stats and special effects.
    /// </summary>
    Epic = 4,

    /// <summary>
    /// Legendary items with exceptional stats and unique abilities.
    /// </summary>
    Legendary = 5,

    /// <summary>
    /// Mythic items - the rarest and most powerful in the game.
    /// </summary>
    Mythic = 6
}
